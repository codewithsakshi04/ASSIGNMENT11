public class DivisionDemo {
    public static void main(String[] args) {
        int a = 5, b = 2;
        double d1 = a / b; // int division
        double d2 = (double)a / b; // double division
        System.out.println("int division: " + d1);
        System.out.println("double division: " + d2);
    }
}
